const WL: Record<string, WLang> = { zh: ZH, en: EN, th: TH, ja: JA };
export const WIZARD_LANGS = ['zh', 'en', 'th', 'ja'] as const;

let wlang = 'th';
export function setWizardLang(l: string) {
    if (WL[l]) wlang = l;
}
export function getWizardLang(): string {
    return wlang;
}
export function wpack(): WLang {
    return WL[wlang] || WL.th;
}
export function wt(key: string): string {
    return wpack().s[key] || key;
}
