const LANG_TEXT_OK = {
    zh: (t) => /[一-鿿]/.test(t) && !/[぀-ヿ]/.test(t), // 含汉字 + 不含假名
    en: (t) => /^[\x00-\x7f]+$/.test(t) && /[A-Za-z]/.test(t), // 纯 ASCII + 含字母
    ja: (t) => /[぀-ヿ]/.test(t), // 含平/片假名
    th: (t) => /[฀-๿]/.test(t), // 含泰文字符
};

// 顺序末尾落在 th(应用默认语言)· 测完不把账号语言偏好留在小众语言上
const LANGS = ['zh', 'en', 'ja', 'th'];

module.exports = { LANG_TEXT_OK, LANGS };
